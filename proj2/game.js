// Function checks for a key press and moves the sprite accordingly
function keyPressEventHandler( key ) 
{
   // Checks for user pressing up, down, left, right arrow keys
   // Up key
   if( key.keyCode == 38 ) 
   {
      // Prevent key from scrolling
      key.preventDefault();
      move_sound.play();
      spider.texture = spiderTexUp;
      
      if( spider.x == 608 && spider.y - 16 == 0 )
      {
         spider.y -= 16;
         endScreen.visible = true;
      }
      
      // If up arrow is pressed, move the player up (unless it will take
      // the user out of bounds! (off stage)
      if( spider.y >= 32 ) 
      {
         if( !testWallCollision( spider, wallList, key.keyCode ) )
         {
            spider.y -= 16;
         }
      }
   }

   // Down key
   if ( key.keyCode === 40 ) 
   {
      key.preventDefault();
      move_sound.play();
      spider.texture = spiderTexDef;
      if( spider.y < renderer.height - 32 ) 
      {
         if( !testWallCollision( spider, wallList, key.keyCode ) )
         {
            spider.y += 16;
         }
      }
   }

   // Left Key
   if( key.keyCode === 37 ) 
   {
      key.preventDefault();
      move_sound.play();
      spider.texture = spiderTexLeft;
      if ( spider.x >= 32 ) 
      {
         if( !testWallCollision( spider, wallList, key.keyCode ) )
         {
            spider.x -= 16;
         }
      }
   }

   // Right key
   if( key.keyCode === 39 ) 
   {
      key.preventDefault();
      move_sound.play();
      spider.texture = spiderTexRight;
      if( spider.x < renderer.width - 32) 
      {
         if( !testWallCollision( spider, wallList, key.keyCode ) )
         {
            spider.x += 16;
         }
      }
   }
}


function testWallCollision( player, listOfWalls, movement )
{
   
   var counter = 0;
   
   while( counter < listOfWalls.length )
   {  
      
      //  Define the variables we'll need to calculate
      let playerCenterX, playerCenterY, currentWallCenterX, currentWallCenterY;
      let currentWall = listOfWalls[counter];

      // Find the center points of each sprite
      playerCenterX = player.x + (player.width / 2);
      playerCenterY = player.y + (player.height / 2);
      currentWallCenterX = currentWall.x + (currentWall.width / 2);
      currentWallCenterY = currentWall.y + (currentWall.height / 2);
      //document.getElementById('limit').innerHTML = ("Wall num: " + counter + ", wall center X: " + currentWallCenterX + ", wall center y: " + currentWallCenterY);
      
      switch( movement )
      {
         // Check up key
         case 38:
            if( playerCenterY - 16 == currentWallCenterY && playerCenterX == currentWallCenterX )
            {
               return true;
            }
            break;
            
         // Check down key
         case 40:
            if( playerCenterY + 16 == currentWallCenterY && playerCenterX == currentWallCenterX )
            {
               return true;
            }
            break;
         
         // Check left key         
         case 37:
            if( playerCenterX - 16 == currentWallCenterX && playerCenterY == currentWallCenterY )
            {
               return true;
            }
            break;
            
         // Check right key
         case 39:
            if( playerCenterX + 16 == currentWallCenterX && playerCenterY == currentWallCenterY )
            {
               return true;
            }
            break;
      }
      
      counter ++;
   }
   
   return false;
}